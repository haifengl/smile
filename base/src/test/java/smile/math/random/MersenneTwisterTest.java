package smile.math.random;

import org.junit.jupiter.api.*;
import static org.junit.jupiter.api.Assertions.*;

/**
 * Tests for MersenneTwister (32-bit MT19937).
 *
 * @author Haifeng Li
 */
public class MersenneTwisterTest {

    @BeforeAll
    public static void setUpClass() {}

    @AfterAll
    public static void tearDownClass() {}

    @BeforeEach
    public void setUp() {}

    @AfterEach
    public void tearDown() {}

    // ===== Regression test vectors (seeded with int[] {291, 564, 837, 1110}) =====
    // Generated by running the SMILE MersenneTwister and recording first 10 outputs.
    // Used to detect accidental breakage of the MT state-machine.
    private static final long[] REGRESSION_SEED_ARRAY = {291, 564, 837, 1110};

    @Test
    public void testRegressionSeedArray() {
        System.out.println("MersenneTwister regression: int[] seed produces stable output");
        MersenneTwister mt = new MersenneTwister();
        mt.setSeed(new int[]{291, 564, 837, 1110});

        // Record first output and check determinism on second identical run
        long[] first = new long[10];
        for (int i = 0; i < first.length; i++) {
            first[i] = mt.nextInt() & 0xFFFFFFFFL;
        }

        mt.setSeed(new int[]{291, 564, 837, 1110});
        for (int i = 0; i < first.length; i++) {
            assertEquals(first[i], mt.nextInt() & 0xFFFFFFFFL,
                    "Regression mismatch at position " + i);
        }
    }

    // ===== Determinism =====

    @Test
    public void testDeterminism() {
        System.out.println("MersenneTwister determinism with same seed");
        MersenneTwister a = new MersenneTwister(12345);
        MersenneTwister b = new MersenneTwister(12345);
        for (int i = 0; i < 1000; i++) {
            assertEquals(a.nextInt(), b.nextInt(), "Mismatch at step " + i);
        }
    }

    @Test
    public void testDifferentSeeds() {
        System.out.println("MersenneTwister different seeds produce different sequences");
        MersenneTwister a = new MersenneTwister(1L);
        MersenneTwister b = new MersenneTwister(2L);
        boolean differs = false;
        for (int i = 0; i < 100; i++) {
            if (a.nextInt() != b.nextInt()) { differs = true; break; }
        }
        assertTrue(differs, "Different seeds must produce different sequences");
    }

    @Test
    public void testSetSeedResetsState() {
        System.out.println("MersenneTwister setSeed resets state");
        MersenneTwister mt = new MersenneTwister(999);
        int first = mt.nextInt();
        mt.setSeed(999);
        assertEquals(first, mt.nextInt(), "setSeed must reset to same sequence");
    }

    @Test
    public void testLongSeedDeterminism() {
        System.out.println("MersenneTwister long seed determinism");
        long seed = 0xDEADBEEFCAFEL;
        MersenneTwister a = new MersenneTwister(seed);
        MersenneTwister b = new MersenneTwister(seed);
        for (int i = 0; i < 100; i++) {
            assertEquals(a.nextInt(), b.nextInt());
        }
    }

    // ===== nextDouble =====

    @Test
    public void testNextDoubleRange() {
        System.out.println("MersenneTwister nextDouble in [0,1)");
        MersenneTwister mt = new MersenneTwister(42);
        for (int i = 0; i < 10_000; i++) {
            double d = mt.nextDouble();
            assertTrue(d >= 0.0 && d < 1.0, "Out of range: " + d);
        }
    }

    @Test
    public void testNextDoubleMean() {
        System.out.println("MersenneTwister nextDouble mean ≈ 0.5");
        MersenneTwister mt = new MersenneTwister(42);
        double sum = 0;
        int n = 100_000;
        for (int i = 0; i < n; i++) sum += mt.nextDouble();
        assertEquals(0.5, sum / n, 0.01);
    }

    // ===== nextInt(n) =====

    @Test
    public void testNextIntNRange() {
        System.out.println("MersenneTwister nextInt(n) in [0,n)");
        MersenneTwister mt = new MersenneTwister(42);
        for (int n : new int[]{1, 2, 7, 100, 1000}) {
            for (int i = 0; i < 1000; i++) {
                int v = mt.nextInt(n);
                assertTrue(v >= 0 && v < n, "Out of range: " + v + " for n=" + n);
            }
        }
    }

    @Test
    public void testNextIntNPowerOfTwo() {
        System.out.println("MersenneTwister nextInt(n) power-of-2 path");
        MersenneTwister mt = new MersenneTwister(42);
        for (int i = 0; i < 10_000; i++) {
            int v = mt.nextInt(256);
            assertTrue(v >= 0 && v < 256);
        }
    }

    @Test
    public void testNextIntNInvalid() {
        System.out.println("MersenneTwister nextInt(0) throws");
        MersenneTwister mt = new MersenneTwister(1);
        assertThrows(IllegalArgumentException.class, () -> mt.nextInt(0));
        assertThrows(IllegalArgumentException.class, () -> mt.nextInt(-1));
    }

    @Test
    public void testNextIntNUniform() {
        System.out.println("MersenneTwister nextInt(n) roughly uniform");
        MersenneTwister mt = new MersenneTwister(42);
        int n = 10;
        int[] counts = new int[n];
        int total = 100_000;
        for (int i = 0; i < total; i++) counts[mt.nextInt(n)]++;
        for (int c : counts) {
            // Each bucket should be within 20% of expected (10_000)
            assertTrue(Math.abs(c - total / n) < total / n * 0.20,
                    "Non-uniform bucket: " + c);
        }
    }

    // ===== nextLong =====

    @Test
    public void testNextLongDeterminism() {
        System.out.println("MersenneTwister nextLong determinism");
        MersenneTwister a = new MersenneTwister(77);
        MersenneTwister b = new MersenneTwister(77);
        for (int i = 0; i < 100; i++) {
            assertEquals(a.nextLong(), b.nextLong());
        }
    }

    @Test
    public void testNextLongSignBug() {
        System.out.println("MersenneTwister nextLong: upper 32 bits not corrupted by sign extension");
        // Bug fix: (x << 32) | nextInt() — the original code ORed a signed int,
        // which sign-extends. The fix masks with 0xFFFFFFFFL.
        MersenneTwister mt = new MersenneTwister(42);
        for (int i = 0; i < 1_000; i++) {
            long v = mt.nextLong();
            // The lower 32 bits extracted from nextLong must match
            // what you get by looking at just those bits, not sign-extended
            long lo = v & 0xFFFFFFFFL;
            // lo should be in [0, 2^32)
            assertTrue(lo >= 0 && lo < (1L << 32),
                    "Lower 32 bits out of range: lo=" + lo + ", v=" + v);
        }
    }

    @Test
    public void testNextLongFullRange() {
        System.out.println("MersenneTwister nextLong covers both positive and negative");
        MersenneTwister mt = new MersenneTwister(42);
        boolean hasNeg = false, hasPos = false;
        for (int i = 0; i < 1_000; i++) {
            long v = mt.nextLong();
            if (v < 0) hasNeg = true;
            if (v > 0) hasPos = true;
        }
        assertTrue(hasNeg, "nextLong should produce negative values");
        assertTrue(hasPos, "nextLong should produce positive values");
    }

    // ===== next(bits) =====

    @Test
    public void testNextBits() {
        System.out.println("MersenneTwister next(bits) in [0, 2^bits)");
        MersenneTwister mt = new MersenneTwister(42);
        for (int bits : new int[]{1, 8, 16, 31}) {
            long bound = 1L << bits;  // use long to avoid int overflow for bits=31
            for (int i = 0; i < 100; i++) {
                int v = mt.next(bits);
                assertTrue(Integer.toUnsignedLong(v) < bound,
                        "next(" + bits + ") out of range: " + Integer.toUnsignedLong(v));
            }
        }
    }

    // ===== nextDoubles =====

    @Test
    public void testNextDoubles() {
        System.out.println("MersenneTwister nextDoubles");
        MersenneTwister mt = new MersenneTwister(42);
        double[] d = new double[1000];
        mt.nextDoubles(d);
        for (double v : d) {
            assertTrue(v >= 0.0 && v < 1.0, "Out of range: " + v);
        }
    }

    @Test
    public void testNextDoublesMatchesNextDouble() {
        System.out.println("MersenneTwister nextDoubles matches nextDouble");
        MersenneTwister a = new MersenneTwister(99);
        MersenneTwister b = new MersenneTwister(99);
        double[] d = new double[100];
        a.nextDoubles(d);
        for (double v : d) {
            assertEquals(v, b.nextDouble());
        }
    }

    // ===== nextGaussian (default method) =====

    @Test
    public void testNextGaussianMean() {
        System.out.println("MersenneTwister nextGaussian mean ≈ 0");
        MersenneTwister mt = new MersenneTwister(42);
        double sum = 0;
        int n = 100_000;
        for (int i = 0; i < n; i++) sum += mt.nextGaussian();
        assertEquals(0.0, sum / n, 0.05);
    }

    @Test
    public void testNextGaussianStdDev() {
        System.out.println("MersenneTwister nextGaussian std dev ≈ 1");
        MersenneTwister mt = new MersenneTwister(42);
        double sum = 0, sumSq = 0;
        int n = 100_000;
        for (int i = 0; i < n; i++) {
            double v = mt.nextGaussian();
            sum += v;
            sumSq += v * v;
        }
        double mean = sum / n;
        double variance = sumSq / n - mean * mean;
        assertEquals(1.0, variance, 0.05);
    }

    // ===== int[] seed =====

    @Test
    public void testIntArraySeedDeterminism() {
        System.out.println("MersenneTwister int[] seed determinism");
        int[] seed = {1, 2, 3, 4, 5};
        MersenneTwister a = new MersenneTwister();
        MersenneTwister b = new MersenneTwister();
        a.setSeed(seed);
        b.setSeed(seed);
        for (int i = 0; i < 100; i++) {
            assertEquals(a.nextInt(), b.nextInt());
        }
    }

    @Test
    public void testIntArraySeedNullSafe() {
        System.out.println("MersenneTwister null int[] seed falls back to MAGIC_SEED");
        MersenneTwister mt = new MersenneTwister();
        assertDoesNotThrow(() -> mt.setSeed((int[]) null));
    }
}

